#Class One
class BMW:
    def brand(self):
        print('BMW is a German brand')
    def type(self):
        print('BMW is a luxury car')
    def engine(self):
        print('BMW cars have powerful engines')
    def speed(self):
        print('BMW cars are known for their speed')
    def price(self):
        print('BMW cars are expensive')
    def safety(self):
        print('BMW cars are very safe')
    def technology(self):
        print('BMW cars have the latest technology')

